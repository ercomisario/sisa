	people.name,
	people.last_name,
	people.birthday,
	medical_records.id as medical_record_id,
	clinical_cases.id as  clinical_case_id,
	appointments.id as appointment_id,
	appointments.reason as reason_appointments,
	medical_consultations.id as medical_consultation_id,
	medical_consultations.description,
	medical_reports.diagnostic,
	medical_reports.id as medical_report_id,
	medical_reports.pathology,
	admissions.reason as reason_admissions,
	medical_orders.id as medical_order_id
from people
left join appointments on appointments.person_id = people.id
left join medical_consultations on medical_consultations.appointment_id = appointments.id
left join medical_records on medical_records.person_id = people.id
left join clinical_cases on clinical_cases.medical_record_id = medical_records.id
left join medical_reports on medical_reports.medical_consultation_id = medical_consultations.id
left join admissions on admissions.medical_report_id = medical_reports.id
left join medical_orders on medical_orders.clinical_case_id = clinical_cases.id
